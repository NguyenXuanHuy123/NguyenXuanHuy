public class student {
        public String maSV;
        public String hoTen;
        public String lop;
    
        public student (String maSV, String hoTen, String lop) {
            this.maSV = maSV;
            this.hoTen = hoTen;
            this.lop = lop;
        }
    
        public String getMaSV() {
            return maSV;
        }
    
        public String getHoTen() {
            return hoTen;
        }
    
        public String getLop() {
            return lop;
        }
    }
    
